package javaproject;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.util.Vector;

import javax.swing.JOptionPane;

// DB연동의 기능의 클래스 
public class AdminMgr {
	// 매번 정보 가져오는 과부하 안걸리게 pool에 만든 인스턴스를 담기
	private DBConnectionMgr pool;

	public AdminMgr() {
		pool = DBConnectionMgr.getInstance();
	}

	
	//DB 전체예약내역 조회
	@SuppressWarnings({ "rawtypes", "unchecked" })
	public Vector selectAll() { 

		Connection con = null;
		PreparedStatement pstmt = null;
		ResultSet rs = null;
		String sql = null;
		Vector data = new Vector<>();

		data.clear();

		try {
			con = pool.getConnection();

			// 쿼리문
			sql = "select * from reservation";
			pstmt = con.prepareStatement(sql);
			rs = pstmt.executeQuery();
			
			while (rs.next()) {
				Vector in = new Vector<String>(); // 1개의 레코드 저장하는 벡터 생성

				String res_no = rs.getString(1);
				String id = rs.getString(2);
				String r_room = rs.getString(3);
				String startdate = rs.getString(4);
				String enddate = rs.getString(5);
				String headcount = rs.getString(6);
				String r_status = rs.getString(7);
				String p_cost = rs.getString(8);


				// 벡터에 각각의 값 추가

				in.add(res_no);
				in.add(id);
				in.add(r_room);
				in.add(startdate);
				in.add(enddate);
				in.add(headcount);
				in.add(r_status);
				in.add(p_cost);
						
				// 전체 데이터를 저장하는 벡터에 in(1명의 데이터 저장) 벡터 추가
				data.add(in);
			}

		} catch (Exception e) {
			e.printStackTrace();
		} finally {
			pool.freeConnection(con, pstmt);
		}
		return data;
	}


	//예약삭제: ReservationAdmin 텍스트필드에서 예약번호만 끌어와서 삭제 실행
	public void delete(String res_no) {
		
		Connection con = null;
		PreparedStatement pstmt = null;
		String sql = null;
		@SuppressWarnings("rawtypes")
		Vector data = new Vector<>();

		data.clear();
		
		try {
			con = pool.getConnection();

			// 쿼리문
			sql = "delete from reservation where res_no = ?";
			pstmt = con.prepareStatement(sql);
			
			pstmt.setString(1, res_no);
			//pstmt.executeUpdate();
			
			int cnt = pstmt.executeUpdate();
			if(cnt==1) 
				JOptionPane.showMessageDialog(null, "예약삭제 완료.");
			else 
				JOptionPane.showMessageDialog(null, "예약삭제 실패."); 

		} catch (Exception e) {
			e.printStackTrace();
		} finally {
			pool.freeConnection(con, pstmt);
		}
	}

}
